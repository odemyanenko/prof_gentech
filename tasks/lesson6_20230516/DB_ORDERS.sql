CREATE TABLE SALESPEOPLE(
       SNUM    numeric(4,0) PRIMARY KEY, 
       SNAME   CHAR(20), 
       CITY    CHAR(20), 
       COMM    numeric(5,2)
);
                                            
CREATE TABLE CUSTOMER(
       CNUM    numeric(4,0) PRIMARY KEY, 
       CNAME   CHAR(20), 
       CITY    CHAR(20), 
       RATING  numeric(3,0)
);

CREATE TABLE ORDERS(
       ONUM    numeric(4,0) PRIMARY KEY, 
       AMT     numeric(7,2), 
       ODATE   DATE, 
       CNUM    numeric(4,0), 
       SNUM    numeric(4,0) 
);


